# CODING RULES (For Python)
#
### NAMING ###
#
#- variables (every type): all lowercase with underscores separating words
#- constants (every type): all uppercase with underscores separating words
#- functions names: all lowercase with underscores separating words
#- class names: capital words with no underscores
#
### ORGANIZATION ###
#
#- every class (object) need it's own file named like the class but with variable naming rules
#- settings file for all the costants
#- main file almost empty
#- every action need it's own function (input, jump, move...)
#- classes have separate functions for image rendering and calculating
#- the function for image rendering is 'draw' and the one for calculating is 'update'
#- class reference inside the class is 'self'